namespace Snap.Enums;

/// <summary>
/// Represents the buttons available on a standard mouse input device.
/// </summary>
public enum MouseButton
{
	/// <summary>
	/// The left mouse button.
	/// </summary>
	Left,

	/// <summary>
	/// The right mouse button.
	/// </summary>
	Right,

	/// <summary>
	/// The middle mouse button, typically the scroll-wheel button.
	/// </summary>
	Middle,
}

