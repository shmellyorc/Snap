namespace Snap.Enums;

/// <summary>
/// Represents the press state of a button.
/// </summary>
public enum BaseButtonPressedState
{
	/// <summary>
	/// No press state is currently active.
	/// </summary>
	None,

	/// <summary>
	/// The button is currently being pressed.
	/// </summary>
	Pressed,

	/// <summary>
	/// The button has been released.
	/// </summary>
	Released,
}
