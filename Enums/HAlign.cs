namespace Snap.Enums;

/// <summary>
/// Specifies horizontal alignment options for positioning elements within a container.
/// </summary>
public enum HAlign
{
	/// <summary>
	/// Aligns the element to the left edge of the container.
	/// </summary>
	Left,

	/// <summary>
	/// Centers the element horizontally within the container.
	/// </summary>
	Center,

	/// <summary>
	/// Aligns the element to the right edge of the container.
	/// </summary>
	Right
}

