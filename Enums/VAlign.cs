namespace Snap.Enums;

/// <summary>
/// Specifies vertical alignment options for positioning elements within a container.
/// </summary>
public enum VAlign
{
	/// <summary>
	/// Aligns the element to the top of the container.
	/// </summary>
	Top,

	/// <summary>
	/// Centers the element vertically within the container.
	/// </summary>
	Center,

	/// <summary>
	/// Aligns the element to the bottom of the container.
	/// </summary>
	Bottom
}
